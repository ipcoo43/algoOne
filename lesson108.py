# 팩토리얼 수열의 합계
# 1! + 2! + 3! + 4! + 5! = 153
# 1! = 1      -> 1
# 2! = 1! * 2 -> 2
# 3! = 2! * 3 -> 6 
# 4! = 3! * 4 -> 24
# 5! = 4! * 5 -> 120
# n = 1,2,3,4,5... n+1
# f = 1!*2,2!*3,3!*4,4!*5... f*n
# sum=(((1!+2!)+3!)+4!)+5!... sum+f

n=1
f=1
sum=1
while n<5:
    n=n+1
    f=f*n
    sum=sum+f
    print(f'n={n}, f={f}, sum={sum}')
print('result :',sum)